#Part 8 // Der Zuweisungsoperator und die der kombinierte Zuweisungsoperator

example_one = 22 #Der einfache Zuweisungsoperator arbeitet von Rechts nach Links und weißt die Variablen rechts
example_two = 22 + 3 #dem Bezeichner Links zu
example_three = (22 - 2) * 3 #Funktioniert auch mit komplexeren ausdrücken und auf einen Wert ausgewertet

example_four = 5
example_five = 5

example_four = example_four + 10 #Variable wurde um 10 erhöht
example_five += 10 #nimmt den Wert der Variable und addiert den Wert rechts oben drauf // kompaktere, schönere schreibweise

example_six = 6
example_six /= 2 #Teilt den Aktuellen Variablen Wert durch den Wert rechts

example_seven = 5
example_seven *= 2 #Multipliziert den Aktuellen Variablen Wertn mit dem Wert rechts
