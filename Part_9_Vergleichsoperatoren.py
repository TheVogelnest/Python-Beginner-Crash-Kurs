#Part 9 // Vergleichsoperatoren und Boolen

print(1 < 5) #Kleiner als Operrator // ergebnis wird als bool ausgegeben (true or false)
print(1 > 5) #Größer als

print(2 >= 5) #Größer als oder gleich Operrator
print(5 <= 5) #Kleiner als oder gleich Operrator

print(3 == 3) #Ist gleich Operrator
print(1 == 3)

print(5 != 4) #Ist Ungleich Operrator (also wenn es nicht das selbe ist ist es ein true)

variable = False #Boolen können auch als Variable verwednet werdet
