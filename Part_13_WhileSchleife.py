#Part 13 // While Schleife

counter = 5

while counter < 10:   #While kopf inkl. Bedingung
    print("Hier steht Code der wiederholt asugeführt wird.") #Schleifenkörper (Codeblock) der bei jedem Durchgang
    counter += 1            #der While Schleife ausgeführt wird, solange die Bedingung der schleife wahr (true) ist.
    #Diese Schleife wird 5x ausgegeben weil es beim start bei 5 ist, was wahr ist also wird die schleife ausgeführt und
    #dies geht weiter bis wir bei 9 sind. also 5,6,7,8,9 = 5

